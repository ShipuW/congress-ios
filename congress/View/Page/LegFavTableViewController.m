-(void)viewDidAppear:(BOOL)animated{
    [super viewDidAppear:animated];
    NSData *getLegislatorsData = [[NSUserDefaults standardUserDefaults] objectForKey:SAVE_LEG_KEY];
    self.listData = [[NSMutableArray alloc]initWithArray:[NSKeyedUnarchiver unarchiveObjectWithData:getLegislatorsData]];
    [self.tableView reloadData];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [self.listData count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    LegislatorTableViewCell *cell = (LegislatorTableViewCell*)[tableView dequeueReusableCellWithIdentifier:@"LegislatorsCell"];
    
    [cell initCell];

    self.info = [self.listData objectAtIndex:indexPath.row];
    
    [cell setCellData:self.info];
    
    return cell;
}

- (CGFloat)tableView:(UITableView *)tableView heightForRowAtIndexPath:(NSIndexPath *)indexPath
{
    return 55;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{

    [tableView deselectRowAtIndexPath:indexPath animated:YES];
    LegislatorDetailViewController *page = [[LegislatorDetailViewController alloc] init];
    LegislatorModel *info = [self.listData objectAtIndex:indexPath.row];
    page.legislatorInfo = info;
    [self.navigationController pushViewController:page animated:YES];
}

