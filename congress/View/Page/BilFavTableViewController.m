-(void)viewDidAppear:(BOOL)animated{
    [super viewDidAppear:animated];
    NSData *getBillsData = [[NSUserDefaults standardUserDefaults] objectForKey:SAVE_BIL_KEY];
    self.listData = [[NSMutableArray alloc]initWithArray:[NSKeyedUnarchiver unarchiveObjectWithData:getBillsData]];
    [self.tableView reloadData];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [self.listData count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    BillTableViewCell *cell = (BillTableViewCell*)[tableView dequeueReusableCellWithIdentifier:@"BillsCell"];
    
    [cell initCell];
    
    self.info = [self.listData objectAtIndex:indexPath.row];
    
    [cell setCellData:self.info];
    
    return cell;
}

- (CGFloat)tableView:(UITableView *)tableView heightForRowAtIndexPath:(NSIndexPath *)indexPath
{
    return 120;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
    BillDetailViewController *page = [[BillDetailViewController alloc] init];
    BillModel *info = [self.listData objectAtIndex:indexPath.row];
    page.billInfo = info;
    [self.navigationController pushViewController:page animated:YES];
}

